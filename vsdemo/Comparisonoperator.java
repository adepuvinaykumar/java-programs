class Comparisonoperator {
    public static void main(String[] args) {
        int x = 15;
        int y = 20;
        Double d = 60.0;

        y -= 2;
        d = d - 1;

        System.out.println(x >= 5);
        System.out.println(y == 10);
        System.out.println(d <= 100.00);
        System.out.println(x > y + 10);
    }
}